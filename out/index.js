export class Somador {
    constructor() {
        this.calcular = (n1, n2) => {
            return n1 + n2;
        };
    }
}
export class Subtrator {
    constructor() {
        this.calcular = (n1, n2) => {
            return n1 - n2;
        };
    }
}
export class Multiplicador {
    constructor() {
        this.calcular = (n1, n2) => {
            return n1 * n2;
        };
    }
}
export class Divisor {
    constructor() {
        this.calcular = (n1, n2) => {
            return n1 / n2;
        };
    }
}
